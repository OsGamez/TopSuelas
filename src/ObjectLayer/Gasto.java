package ObjectLayer;

public class Gasto {

    private String Mes;
    private int Año;
    private double Mo;
    private double Gf;
    private double Ga;
    private double Mi;
    private String M;

    public Gasto() {
    }

    public String getM() {
        return M;
    }

    public void setM(String M) {
        this.M = M;
    }

    
    

    public String getMes() {
        return Mes;
    }

    public void setMes(String Mes) {
        this.Mes = Mes;
    }

    public int getAño() {
        return Año;
    }

    public void setAño(int Año) {
        this.Año = Año;
    }

    public double getMo() {
        return Mo;
    }

    public void setMo(double Mo) {
        this.Mo = Mo;
    }

    public double getGf() {
        return Gf;
    }

    public void setGf(double Gf) {
        this.Gf = Gf;
    }

    public double getGa() {
        return Ga;
    }

    public void setGa(double Ga) {
        this.Ga = Ga;
    }

    public double getMi() {
        return Mi;
    }

    public void setMi(double Mi) {
        this.Mi = Mi;
    }
}
